    private void fillAllFields(){
        nameField.setText(passiveProfile.getNom());
        firstNameField.setText(passiveProfile.getPrenom());
        loginField.setText(passiveProfile.getLogin());
        pwdField.setText(passiveProfile.getPwd());
        statutComboBox.setSelectedItem(passiveProfile.getStatut());
    }
    
    private void parameters(){
        if(!activeProfile.getStatut().equals("Root"))
            statutComboBox.setEnabled(false);
        if(activeProfile.getStatut().equals("Visitor"))
            editButton.setEnabled(false);
    }
    
