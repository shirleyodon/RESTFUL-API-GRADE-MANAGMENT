    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if(estVide(nameField) || estVide(firstNameField) || estVide(loginField) || estVide(pwdField)){
            JOptionPane.showMessageDialog(null, "Au moins un des champs est laissé vide", "Champ(s) vide(s)", JOptionPane.WARNING_MESSAGE);
        }else{
            if(JOptionPane.showConfirmDialog(null, "Voullez-vous enregistrer les modifications apportées au profil de l'utilisateur '"+passiveProfile.getLogin()+"' ?",
                "Confirmation de mise à jour", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION){
                String pwd=new String();

                for(char kter: pwdField.getPassword())
                    pwd+=kter;

                Utilisateur newProfile=new Utilisateur(nameField.getText(), firstNameField.getText(), loginField.getText(), pwd, statutComboBox.getSelectedItem().toString());

                Integer updated=service.put(passiveProfile.getLogin(), newProfile);

                switch(updated){
                    case -1:
                        JOptionPane.showMessageDialog(null, "Le login '"+loginField.getText()+"' existe déjà", "Erreur de mise à jour", JOptionPane.ERROR_MESSAGE);
                        break;
                    case 1:
                        JOptionPane.showMessageDialog(null, "Les modifications ont été enregistrées avec succès", "Succès de mise à jour", JOptionPane.INFORMATION_MESSAGE);
                        registreService.saveToLog(activeProfile.getLogin(), "MODIFICATION", newProfile.getLogin());
                        passiveProfile=newProfile;
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "Echec de l'enregistrement des modifications", "Erreur de mise à jour", JOptionPane.ERROR_MESSAGE);
                }

                /*
                * Il est impossible de tombé sur updated==0 puisque cette fenêtre s'ouvre à partir d'un profil qui existe
                */
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

