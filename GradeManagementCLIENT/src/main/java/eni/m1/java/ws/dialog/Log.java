    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        try{
            int selectedRowIndex=logTable.getSelectedRow();
            String selectedId=model.getValueAt(selectedRowIndex, 0).toString();
            
            if(JOptionPane.showConfirmDialog(null, "Voullez-vous vraiment supprimer le registre ID = '"+selectedId+ "' ?", "Confirmation de suppression",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION){
                int deleted=service.delete(selectedId);
                
                if(deleted==1){
                    JOptionPane.showMessageDialog(null, "Le registre ID = '"+selectedId+"' a Ã©tÃ© supprimÃ© avec succÃ¨s", "SuccÃ¨s de suppression", JOptionPane.INFORMATION_MESSAGE);
                    model.removeRow(selectedRowIndex);
                    logTable.setModel(model);
                }else
                    JOptionPane.showMessageDialog(null, "Echec de la suppression du registre ID = '"+selectedId+"'", "Erreur de suppression", JOptionPane.ERROR_MESSAGE);
            }
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Aucun registre n'a Ã©tÃ© selectionnÃ©", "Erreur de suppression", JOptionPane.ERROR_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage(), "Erreur de suppression", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

