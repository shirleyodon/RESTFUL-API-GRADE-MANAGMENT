    private void initModel(){
        model=new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        
        model.addColumn("Id");
        model.addColumn("Date");
        model.addColumn("Heure");
        model.addColumn("Login");
        model.addColumn("Action");
        model.addColumn("Cible");
    }
    
    private void fillModel(Registre[] list){
        model.setRowCount(0);
        for(Registre reg: list){
            model.addRow(
                    new Object[]{
                        reg.getId(),
                        reg.getDate(),
                        reg.getHeure(),
                        reg.getLogin(),
                        reg.getAction(),
                        reg.getCible()
                    }
            );
        }
        logTable.setModel(model);
    }

    private void initService(){
        service=new RegistreService();
        fillModel(service.getAll());
    }
    
